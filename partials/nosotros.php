    <h1>Sobre Nosotros</h1>
    <p>El Colegio Hogwarts de Magia y Hechicería, ubicado en Escocia, es una de las escuelas de magia más prestigiosas del mundo mágico. Fundada hace más de mil años por los cuatro grandes hechiceros de la época: Godric Gryffindor, Helga Hufflepuff, Rowena Ravenclaw y Salazar Slytherin.</p>
    <p>Nuestra misión es proporcionar una educación mágica de alta calidad a jóvenes brujas y magos, enseñándoles las artes de la magia, la historia mágica, la defensa contra las artes oscuras y mucho más.</p>
    <p>En Hogwarts, nuestros estudiantes no solo adquieren conocimientos mágicos, sino que también desarrollan amistades duraderas y descubren sus propias habilidades y fortalezas en un ambiente seguro y estimulante.</p>
    <p>¡Únete a nosotros en esta emocionante aventura en el mundo de la magia!</p>
